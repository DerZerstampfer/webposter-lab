import { Webposter } from '@/components/Webposter'
import { prisma } from '@/lib/db'
import { unstable_cache } from 'next/cache'

export const getLatestMovieposters = unstable_cache(
  async () => {
    const webposters = await prisma.webposter.findMany({
      where: {
        published: true,
      },
      orderBy: {
        createdAt: 'desc',
      },
      take: 16,
    })
    return webposters
  },
  ['latestWebposters'],
  {
    revalidate: 60,
    tags: ['latestWebposters'],
  },
)

export const Explore = async () => {
  const webposters = await getLatestMovieposters()

  return (
    <div>
      <div className="mb-8 flex flex-col gap-2">
        <h4 className="text-5xl font-semibold md:text-6xl">Explore</h4>
        <p className="font-mono text-lg text-muted-foreground">
          Here are some of the latest posters generated by the community.
        </p>
      </div>
      <div className="grid grid-cols-1 gap-x-4 gap-y-6 sm:grid-cols-2 md:grid-cols-3 md:gap-y-10 lg:grid-cols-4">
        {webposters.map((webposter) => (
          <Webposter key={webposter.id} webposter={webposter} />
        ))}
      </div>
    </div>
  )
}
